def hanoy(n,a,b,c):
    if n > 0:
        hanoy(n-1,a,c,b,)
        print('Диск ', n, 'переміщений з ', a, 'на ', c)
        hanoy(n-1, b,a,c)
    n += 1


n = 3
hanoy(n,'A','B','C')
